import numpy as np
import matplotlib.pyplot as plt

M_ini = np.array([5., 9., 15., 25., 40., 60., 80., 120., 200., 300., 400., 500.])   #Data from Table 4 of Schaerer (2002)
t_life = np.array([6.19e7, 2.022e7, 1.04e7, 6.459e6, 3.864e6, 3.464e6, 3.012e6, 2.521e6, 2.204e6, 2.047e6, 1.974e6, 1.899e6])
Q_H = np.array([1.848e45, 2.807e47, 1.861e48, 7.583e48, 2.469e49, 4.795e49, 7.73e49, 1.391e50, 2.624e50, 3.754e50, 5.247e50, 6.802e50])
Q_LW = np.array([3.76e46, 3.785e47, 1.76e48, 6.419e48, 2.123e49, 3.848e49, 6.565e49, 1.161e50, 2.487e50, 4.373e50, 5.99e50, 7.933e50])
print(len(M_ini), len(t_life), len(Q_H))
eta_III = (Q_H * t_life * 3.154e7) / (M_ini * 1.989e30 / 1.6736e-27)  #Number of ionizing/LW photons per baryon used in J_LW 
eta_LW = (Q_LW * t_life * 3.154e7) / (M_ini * 1.989e30 / 1.6736e-27)

coeffs_life = np.polyfit(np.log10(M_ini), np.log10(t_life), 3)  #Coefficient values for fitting data
coeffs_ion = np.polyfit(np.log10(M_ini), np.log10(Q_H), 5)
coeffs_LW = np.polyfit(np.log10(M_ini), np.log10(Q_LW), 5)
coeffs_eta = np.polyfit(np.log10(M_ini), np.log10(eta_III), 5)
coeffs_eLW = np.polyfit(np.log10(M_ini), np.log10(eta_LW), 5)
print('Lifetime: ', coeffs_life)
print('Ionizing: ', coeffs_ion)
print('LW: ', coeffs_LW)
print('Eta_i: ', coeffs_eta)
print('Eta_LW: ', coeffs_eLW)
fit_life = 10.**(coeffs_life[0]*np.log10(M_ini)**3. + coeffs_life[1]*np.log10(M_ini)**2. + coeffs_life[2]*np.log10(M_ini) + coeffs_life[3])   #Fits
fit_ion = 10.**(coeffs_ion[0]*np.log10(M_ini)**5. + coeffs_ion[1]*np.log10(M_ini)**4. + coeffs_ion[2]*np.log10(M_ini)**3. + coeffs_ion[3]*np.log10(M_ini)**2. + coeffs_ion[4]*np.log10(M_ini) + coeffs_ion[5])
fit_LW = 10.**(coeffs_LW[0]*np.log10(M_ini)**5. + coeffs_LW[1]*np.log10(M_ini)**4. + coeffs_LW[2]*np.log10(M_ini)**3. + coeffs_LW[3]*np.log10(M_ini)**2. + coeffs_LW[4]*np.log10(M_ini) + coeffs_LW[5])
fit_eLW = 10.**(coeffs_eLW[0]*np.log10(M_ini)**5. + coeffs_eLW[1]*np.log10(M_ini)**4. + coeffs_eLW[2]*np.log10(M_ini)**3. + coeffs_eLW[3]*np.log10(M_ini)**2. + coeffs_eLW[4]*np.log10(M_ini) + coeffs_eLW[5])
fit_eta = 10.**(coeffs_eta[0]*np.log10(M_ini)**5. + coeffs_eta[1]*np.log10(M_ini)**4. + coeffs_eta[2]*np.log10(M_ini)**3. + coeffs_eta[3]*np.log10(M_ini)**2. + coeffs_eta[4]*np.log10(M_ini) + coeffs_eta[5])
ratio_life, ratio_ion, ratio_LW, ratio_eLW, ratio_eta = t_life/fit_life, Q_H/fit_ion, Q_LW/fit_LW, eta_LW/fit_eLW, eta_III/fit_eta  #Ratio of fits wrt data
coeffs_life = np.concatenate(([0., 0.], coeffs_life))
print(coeffs_life, len(coeffs_life))
#np.save('./Schaerer_PopIII_Fit_Coeffs.npy', np.array([coeffs_life, coeffs_ion, coeffs_LW, coeffs_eLW, coeffs_eta]), allow_pickle=True)

M_fine = np.linspace(1., 1000., 1000)
fine_life = 10.**(coeffs_life[0]*np.log10(M_fine)**5. + coeffs_life[1]*np.log10(M_fine)**4. +coeffs_life[2]*np.log10(M_fine)**3. + coeffs_life[3]*np.log10(M_fine)**2. + coeffs_life[4]*np.log10(M_fine) + coeffs_life[5])
fine_ion = 10.**(coeffs_ion[0]*np.log10(M_fine)**5. + coeffs_ion[1]*np.log10(M_fine)**4. + coeffs_ion[2]*np.log10(M_fine)**3. + coeffs_ion[3]*np.log10(M_fine)**2. + coeffs_ion[4]*np.log10(M_fine) + coeffs_ion[5])
fine_LW = 10.**(coeffs_LW[0]*np.log10(M_fine)**5. + coeffs_LW[1]*np.log10(M_fine)**4. + coeffs_LW[2]*np.log10(M_fine)**3. + coeffs_LW[3]*np.log10(M_fine)**2. + coeffs_LW[4]*np.log10(M_fine) + coeffs_LW[5])
fine_eLW = 10.**(coeffs_eLW[0]*np.log10(M_fine)**5. + coeffs_eLW[1]*np.log10(M_fine)**4. + coeffs_eLW[2]*np.log10(M_fine)**3. + coeffs_eLW[3]*np.log10(M_fine)**2. + coeffs_eLW[4]*np.log10(M_fine) + coeffs_eLW[5])
fine_eta = 10.**(coeffs_eta[0]*np.log10(M_fine)**5. + coeffs_eta[1]*np.log10(M_fine)**4. + coeffs_eta[2]*np.log10(M_fine)**3. + coeffs_eta[3]*np.log10(M_fine)**2. + coeffs_eta[4]*np.log10(M_fine) + coeffs_eta[5])
val = np.argmin(np.abs(M_ini-35.69))
print('Fiducial (35.69 M_sun) values: ', t_life[val], Q_H[val], Q_LW[val], eta_LW[val], eta_III[val])

fig, ax = plt.subplots(4,1,sharex=True)
ax[0].loglog(M_ini, t_life, '-o', label='S02 Data')
ax[0].loglog(M_ini, fit_life, ls='--', label='Fit')
ax[0].loglog(M_fine, fine_life, ls=':', label='Fine Fit')
ax[0].set_ylabel('Stellar lifetime (yr)')
ax[1].loglog(M_ini, Q_H, '-o', label='Ion')
ax[1].loglog(M_ini, fit_ion, ls='--')
ax[1].loglog(M_fine, fine_ion, ls=':', label='Ion Fit')
ax[1].loglog(M_ini, Q_LW, '-o', label='LW')
ax[1].loglog(M_ini, fit_LW, ls='--')
ax[1].loglog(M_fine, fine_LW, ls=':', label='LW Fit')
ax[1].set_ylabel('Ionizing/LW Photons (s^-1)')
ax[2].loglog(M_ini, eta_III, '-o', label='Ion')
ax[2].loglog(M_ini, fit_eta, ls='--')
ax[2].loglog(M_fine, fine_eta, ls=':', label='Ion Fit')
ax[2].loglog(M_ini, eta_LW, '-o', label='LW')
ax[2].loglog(M_ini, fit_eLW, ls='--')
ax[2].loglog(M_fine, fine_eLW, ls=':', label='LW Fit')
ax[2].set_ylabel('# of Ion/LW Photons/Baryon')
ax[3].plot(M_ini, ratio_life, '-o', markersize=3, label='Lifetime')
ax[3].plot(M_ini, ratio_ion, '-o', markersize=3, label='Ionizing Photons')
ax[3].plot(M_ini, ratio_LW, '-o', markersize=3, label='LW Photons')
ax[3].plot(M_ini, ratio_eta, '-o', markersize=3, label='Ion Photons/Baryon')
ax[3].plot(M_ini, ratio_eLW, '-o', markersize=3, label='LW Photons/Baryon')
ax[3].axhline(1., ls='--', color='black')
ax[3].set_ylabel('Ratio')
ax[3].set_xlabel('Mass of PopIII Star (M_sun)')
ax[0].set_ylim(1e6, 1e8)
ax[1].set_ylim(1e44, 1e52)
ax[2].set_ylim(200, 3e5)
ax[0].legend(fontsize=8)
ax[1].legend(fontsize=8)
ax[2].legend(fontsize=8)
ax[3].legend(fontsize=8)
ax[0].tick_params(which='both', left=True, right=True, top=True, direction="in")
ax[1].tick_params(which='both', left=True, right=True, top=True, direction="in")
ax[2].tick_params(which='both', left=True, right=True, top=True, direction="in")
ax[3].tick_params(which='both', left=True, right=True, top=True, direction="in")
fig.subplots_adjust(hspace=0.07)
fig.set_size_inches(6, 10)
plt.tight_layout()
